<template>
  <div class="body">
  
    <div class="box" v-show="regStep==1">
      <div class="input">
        <div class="title">手机号：</div>
        <el-input placeholder="请输入手机号" autofocus @blur="verifyTel" v-model="regData.phone">
        </el-input>
      </div>
  
      <div class="input sms">
        <div class="title">验证码：</div>
        <el-input placeholder="请输入短信验证码" autofocus v-model="regData.code">
        </el-input>
        <el-button type="info" @click="getSms">获取短信</el-button>
      </div>
  
      <div class="input">
        <div class="title">密码：</div>
        <el-input type="password" placeholder="请输入密码" :minlength='6' @keyup.enter.native="reg" v-model="regData.password">
        </el-input>
      </div>
  
      <div class="btn">
        <el-button type="info" @click="$router.push('/login')">返回登陆</el-button>
        <el-button type="warning" @click="reg">注册</el-button>
      </div>
  
    </div>
  
    <div class="box afterReg" v-show="regStep==2">
      <div class="info">
        <div>您的登录帐号</div>
        <div class="phone">13900000006</div>
      </div>
  
      <div class="btn">
        <el-button type="info" @click="$router.push('/')">进入主页</el-button>
      </div>
      <div class="btn">
        <el-button type="warning" @click="regStep=3">完善资料</el-button>
      </div>
    </div>

    <div class="box afterReg" v-show="regStep==3">
      <div class="input">
        <div class="title">姓名：</div>
        <el-input placeholder="请输入真实姓名" autofocus @blur="verifyTel" v-model="regData.phone">
        </el-input>
      </div>

      <div class="input">
        <div class="title">学生ID：</div>
        <el-input placeholder="请输入手机号" autofocus @blur="verifyTel" v-model="regData.phone">
        </el-input>
      </div>

      <div class="btn">
        <el-button type="warning" @click="regStep=3">完善资料</el-button>
      </div>
    </div>
  
  </div>
</template>

<script>
export default {
  name: 'reg',
  components: {},
  data() {
    return {
      isVerified: Boolean(true),
      regData: {
        phone: '',
        code: '',
        password: ''
      },
      regStep: 2
    }
  },
  methods: {
    verifyTel() {
      if (this.regData.phone.length != 11) {
        this.$message.error('手机号格式错误')
        return false
      } else {
        this.isVerified = Boolean(false)
        return true
      }
    },
    verifyPw() {
      if (this.regData.password.length < 6) {
        this.$message.error('密码不能少于6位数')
        return false
      } else {
        return true
      }
    },
    getSms() {
      if (this.regData.phone == '') {
        this.$message.error('手机号不能为空')
      } else {
        this.$API.getRegSms(this.regData.phone).then(res => {
          this.$message.success('获取验证码成功，请查收短信')
        }).catch(err => {
          this.$message.error(err.msg)
        })
      }
    },
    reg() {
      this.regData.role = 2
      if (this.regData.code == '') {
        this.$message.error('短信验证码不能为空')
      } else if (this.regData.password.length < 6) {
        this.$message.error('密码不能小于6位数')
      } else {
        this.$API.userReg(this.regData).then(res => {
          this.$message.success('注册成功，正在自动登陆')
          let loginData = {}
          loginData.phone = this.regData.phone
          loginData.password = this.regData.password
          this.$store.dispatch('login', loginData).then(res => {
            this.$message('登录成功')
            this.$router.push('/')
          })
        }).catch(err => {
          this.$message.error(err.msg)
        })
      }
    },
  },
  created() {

  },
  mounted() {

  },
}
</script>

